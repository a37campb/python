function dw = f5f( t, w )
    a1 =  0.3;
    a2 = -0.1;
    a3 =  0.5;
    a4 =  0.8;
    b  =  1.0;
 
    c1 =  0.2;
    c2 = -0.6;
    c3 =  0.4;
    c4 =  0.7;
    d  =  0.9;

    dw = [w(2);
        b - a4*w(4) - a3*w(3) - a2*w(2) - a1*w(1);
        w(4);
        d - c4*w(4) - c3*w(3) - c2*w(2) - c1*w(1)];
end
